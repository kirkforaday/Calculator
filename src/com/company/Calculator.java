package com.company;

import java.util.Scanner;

public class Calculator {
    public static double i;

    public static void calculator() { }
        //Addition Feature
        public static double add(double i, double ii){
            return (i + ii);
        }
        //Subtraction Feature
        public static double subtract(double i, double ii){
            return (i - ii);
        }
        //Multiplication Feature
        public static double multiply(double i, double ii){
            return (i * ii);
        }
        //Division Feature
        public static double divide(double i, double ii){
            return (i / ii);
        }
        //Square Feature
        public static double square(double i){
            return (i * i);
        }
    }

